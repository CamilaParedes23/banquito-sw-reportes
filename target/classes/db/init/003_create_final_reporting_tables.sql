    "MONTO" NUMERIC(18, 2) NOT NULL,
    "MONEDA" VARCHAR(3) NOT NULL,
    "ESTADO_FINAL" VARCHAR(40) NOT NULL,
    "COBRABLE" BOOLEAN NOT NULL,
    "CODIGO_RECHAZO" VARCHAR(80),
